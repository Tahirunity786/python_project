# This is Modifying a string
# The proces is done through many built-in function
a = " This is a string, It is modifying through different function"
#  First technique to convert upper cast
print(a.upper())
print("\n")
# Second technique to convert lower cast
print(a.lower())
print("\n")
# this technique to convert remove first spaces
p = a.strip()
print(p+"I'm right now")
print("\n")
# This technique to highlight some in console
print(a.strip(","))
print("\n")


